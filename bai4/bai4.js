"use strict";
function printTypeOfArgument(argument) {
    console.log("Kiểu dữ liệu của đối số là:", typeof argument);
}
// Ví dụ sử dụng hàm
printTypeOfArgument(5); // Output: Kiểu dữ liệu của đối số là: number
printTypeOfArgument("Hello"); // Output: Kiểu dữ liệu của đối số là: string
printTypeOfArgument(true); // Output: Kiểu dữ liệu của đối số là: boolean
printTypeOfArgument({ key: "value" }); // Output: Kiểu dữ liệu của đối số là: object
